package PositionOf3DModel;

public class Point3D {

    private int x;
    private int y;
    private int z;

    public Point3D(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    public Point3D(){
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }
}
